// Get rebalance dates
function getRebalanceDates() {
  const today = new Date();
  const year = today.getFullYear();
  
  // S&P 500 typically rebalances on third Friday of March, June, September, December
  // But sometimes it can be adjusted - June 2025 was on June 6 (first Friday)
  const rebalanceMonths = [2, 5, 8, 11]; // 0-indexed months
  
  let lastRebalance = null;
  let nextRebalance = null;
  
  // Check this year and next year
  for (let y = year - 1; y <= year + 1; y++) {
    for (let month of rebalanceMonths) {
      const rebalanceDate = getThirdFriday(y, month);
      
      // Special case for June 2025 - it was on June 6
      if (y === 2025 && month === 5) {
        const june6 = new Date(2025, 5, 6);
        if (june6 <= today && (!lastRebalance || june6 > lastRebalance)) {
          lastRebalance = june6;
        }
        if (june6 > today && !nextRebalance) {
          nextRebalance = june6;
        }
      } else {
        if (rebalanceDate <= today && (!lastRebalance || rebalanceDate > lastRebalance)) {
          lastRebalance = rebalanceDate;
        }
        if (rebalanceDate > today && !nextRebalance) {
          nextRebalance = rebalanceDate;
        }
      }
    }
  }
  
  return { lastRebalance, nextRebalance };
}

function getThirdFriday(year, month) {
  const firstDay = new Date(year, month, 1);
  const firstFriday = new Date(firstDay);
  
  // Find first Friday
  while (firstFriday.getDay() !== 5) {
    firstFriday.setDate(firstFriday.getDate() + 1);
  }
  
  // Add 14 days to get third Friday
  firstFriday.setDate(firstFriday.getDate() + 14);
  return firstFriday;
}

