// Function to get rebalance dates (unchanged)
function getRebalanceDates() {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  
  function getThirdFriday(year, month) {
    const date = new Date(year, month, 1);
    const firstDay = date.getDay();
    const firstFriday = firstDay <= 5 ? 5 - firstDay : 12 - firstDay;
    const thirdFriday = firstFriday + 14;
    return new Date(year, month, thirdFriday);
  }
  
  const rebalanceMonths = [2, 5, 8, 11]; // March, June, September, December
  const dates = [];
  
  // Get current year rebalance dates
  const currentYear = today.getFullYear();
  for (const month of rebalanceMonths) {
    const date = getThirdFriday(currentYear, month);
    dates.push(date);
  }
  
  // Also get first rebalance date of next year
  dates.push(getThirdFriday(currentYear + 1, 2));
  
  // Handle June 2025 exception
  const june2025 = dates.find(d => d.getFullYear() === 2025 && d.getMonth() === 5);
  if (june2025) {
    june2025.setDate(6); // June 6, 2025
  }
  
  // Find last and next rebalance dates
  let lastRebalance = null;
  let nextRebalance = null;
  
  for (const date of dates) {
    if (date <= today) {
      lastRebalance = date;
    } else if (!nextRebalance) {
      nextRebalance = date;
    }
  }
  
  // If no last rebalance found in current year, get December of previous year
  if (!lastRebalance) {
    lastRebalance = getThirdFriday(currentYear - 1, 11);
  }
  
  return { lastRebalance, nextRebalance };
}

